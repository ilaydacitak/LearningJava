public class Loops {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int i ;
        for (i=0 ; i<10 ; i++){
            System.out.println("A");
                   
        }
        //
        int a = 0 ;
        do {
            System.out.println("B");
            a++ ;
        }
        while (a<10) ;
        //
        int x =0 ;
        while (x<10){
            System.out.println("C");
            x++ ;
        }
    }
    
}
